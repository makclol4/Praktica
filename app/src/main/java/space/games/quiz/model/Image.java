package space.games.quiz.model;

public class Image {
    private String name;
    private String src;
    private int value;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSrc() {
        return src;
    }

    public void setSrc(String src) {
        this.src = src;
    }

    public int getValue() {
        return value;
    }

    public void setValue(int weight) {
        this.value = weight;
    }
}
